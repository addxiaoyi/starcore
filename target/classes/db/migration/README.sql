-- StarCore 数据库迁移脚本指南
-- ============================================
--
-- 本目录包含 StarCore 的所有 Flyway 数据库迁移脚本
--
-- 命名规范：
-- V{版本号}__{描述}.sql
-- - 版本号：数字，如 1, 2, 3...
-- - 描述：使用下划线分隔的简短描述
--
-- ============================================
-- 迁移版本说明
-- ============================================
--
-- V1__initial_schema.sql
--   初始数据库架构
--   - 核心状态表 (nation, diplomacy, policy, resource, technology, treasury, war, officer, event, resolution, territory)
--   - 玩家余额表
--   - 经济事务日志表
--
-- V2__map_module.sql
--   地图模块扩展
--   - 地图区块数据表
--   - 地图标记表
--
-- V3__performance_indexes.sql
--   性能优化索引
--   - 各类索引以提升查询性能
--
-- V4__bankruptcy_and_i18n.sql
--   破产管理系统
--   - 破产状态表
--   - 玩家语言设置表
--   - 转账配置表
--
-- V5__territory_persistence.sql
--   领土系统持久化
--   - 领土主表
--   - 成员表
--   - 子区域表
--   - 权限表
--
-- V6__technology_research.sql
--   科技研究进度持久化
--   - 研究进度主表
--
-- V7__social_persistence.sql
--   社交系统持久化
--   - 好友关系/请求表
--   - 公会/成员/邀请表
--   - 派对/成员/邀请表
--   - 黑名单表
--   - 玩家在线状态表
--
-- V8__pet_module.sql (新)
--   宠物/坐骑模块
--   - 宠物/坐骑基础表
--   - 技能/进化/装备表
--   - 皮肤表
--   - 迁移日志表
--   - 备份记录表
--
-- V9__ranking_and_audit.sql (新)
--   排行榜系统和审计
--   - 排行榜快照表
--   - KDA 统计表
--   - 交易订单/历史表
--   - 审计日志表
--   - 委托任务表
--
-- ============================================
-- 命令使用说明
-- ============================================
--
-- 插件启动时自动执行迁移
-- 可用命令：
--   /dbmigrate info      - 查看迁移状态
--   /dbmigrate migrate   - 执行迁移
--   /dbmigrate undo      - 回滚最后一次迁移
--   /dbmigrate redo      - 重做迁移
--   /dbmigrate validate  - 验证数据库
--   /dbmigrate repair    - 修复数据库
--   /dbmigrate backup    - 创建备份
--   /dbmigrate history   - 查看历史
--   /dbmigrate pending   - 查看待执行迁移
--
-- ============================================
-- 回滚说明
-- ============================================
--
-- Flyway 开源版本不支持 UNDO 功能
-- 如需回滚，请：
-- 1. 使用 /dbmigrate backup 创建备份
-- 2. 手动执行 rollback_guide.sql 中的对应回滚脚本
-- 3. 记录回滚操作
--
-- ============================================
-- 最佳实践
-- ============================================
--
-- 1. 每次部署前创建数据库备份
-- 2. 先在测试环境验证迁移脚本
-- 3. 使用版本控制管理迁移脚本
-- 4. 避免修改已执行的迁移脚本
-- 5. 使用幂等性语句 (CREATE TABLE IF NOT EXISTS)
